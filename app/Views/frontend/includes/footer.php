	$('.Subscribe').click(function(event) {
		event.preventDefault();
		let subscriber = $('#subscribe_email').val();
		if (!subscriber) {
			new Notify({
				title: 'Error',
				text: 'Please enter an email address.',
				status: 'error',
				autoclose: true,
				autotimeout: 3000
			});
			return;
		}
		$.ajax({
			url: 'addsubscribers',
			type: 'POST',
			data: {
				subscriber: subscriber
			},
			dataType: 'JSON',
			beforeSend: function() {
				$('.Subscribe').text('Wait...');
			},
			success: function(res) {
				if (res.status == 'success') {
					new Notify({
						title: 'Notify Title',
						text: res.message,
						type: 'success',
						autoclose: true,
						autotimeout: 3000
					})
					$('.Subscribe').text('Subscribe');
				      $('#myForm')[0].reset();
				} else {
					new Notify({
						title: 'Notify Title',
						text: res.message,
						status: 'error',
						type: 'error',
						autoclose: true,
						autotimeout: 3000
					})
					$('.Subscribe').text('Subscribe');
				}
			},
			error: function(err) {
				console.log(err);
			}
		})
	});
